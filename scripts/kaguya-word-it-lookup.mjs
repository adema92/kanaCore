function inferItalian(key) {
  if (/(teiru|deiru|tteiru)$/.test(key))
    return 'Forma in -te + essere: azione in corso, stato risultante o passivo'
  if (/mashitaga$/.test(key)) return 'Passato cortese con concessione («ma», «eppure»)'
  if (/mashita$/.test(key)) return 'Passato cortese (azione o stato compiuto)'
  if (/mashou$/.test(key)) return 'Invito o ipotesi cortese («andiamo», «forse»)'
  if (/masen$/.test(key)) return 'Presente cortese negativo'
  if (/masu$/.test(key)) return 'Presente o abitudine (stile cortese)'
  if (/deshita$/.test(key)) return 'Copula al passato (cortese)'
  if (/desu$/.test(key)) return 'Copula cortese (è, sono)'
  if (/darou$/.test(key)) return 'Supposizione o probabilità'
  if (/(rareru|reru)$/.test(key) && key.length > 5)
    return 'Potenziale, passivo o rispetto (suffisso -rareru/-reru)'
  if (/saseru$/.test(key)) return 'Causativo («far fare»)'
  if (/katta$/.test(key)) return 'Passato informale o forma -i aggettivo + passato'
  if (/kute$/.test(key)) return 'Forma in -te (collegamento tra proposizioni)'
  if (/sugiru$/.test(key)) return 'Eccesso («troppo»)'
  if (/sou$/.test(key) && key.length > 3) return 'Apparenza o voce («sembra», «si dice»)'
  if (/ppana$/.test(key)) return 'Enfasi («proprio, davvero»)'
  return null
}

/**
 * @param {string} romajiRaw token as in romaji reference (may include punctuation)
 * @returns {string} non-empty Italian gloss for tooltips
 */
export function lookupWordIt(romajiRaw) {
  const key = normalizeKey(romajiRaw)
  if (!key) return 'Punteggiatura'
  const particle = glossItForRomajiWord(romajiRaw)
  if (particle) return particle
  if (LEX[key]) return LEX[key]
  const inferred = inferItalian(key)
  if (inferred) return inferred
  return 'Voce nel racconto: il senso preciso è nella traduzione intera della frase'
}
